# -*- coding: utf-8 -*-
"""업로드 표준 양식(플랫폼) 생성. 현재 원료마스터를 담아 바로 채울 수 있게 제공."""
from io import BytesIO
import pandas as pd
from . import db


def _bytes(sheets: dict) -> bytes:
    buf = BytesIO()
    with pd.ExcelWriter(buf, engine="openpyxl") as w:
        for name, df in sheets.items():
            df.to_excel(w, sheet_name=name, index=False)
    return buf.getvalue()


def price_template() -> bytes:
    """단가·사용량 통합 업로드 양식. 매달 아래에 이어붙여 통합본으로 업로드."""
    mm = db.load_material_master()[["ERP코드", "대표원료명"]].rename(
        columns={"ERP코드": "원료코드", "대표원료명": "원료명"})
    example = pd.DataFrame({
        "년월": ["2026-07", "2026-07"],
        "원료코드": ["1010101", "1050801"],
        "원료명": ["닭고기 MDCM", "녹색 완두 통곡"],
        "단가(원/kg)": [700, 882],
        "사용량(kg)": [130000, 114000],
        "금액(원)": [91000000, 100548000],
    })
    guide = pd.DataFrame({"작성법": [
        "■ 목적: 매달 원료 실적(단가·사용량·금액)을 한 파일에 계속 이어붙여 통합 업로드.",
        "",
        "■ 규칙",
        " 1. 한 행 = 한 원료의 '한 달' 실적.",
        " 2. [년월] YYYY-MM 형식. 예: 2026-07",
        " 3. [원료코드] 는 조인 기준 키. '원료마스터_참고' 시트에서 코드를 그대로 사용.",
        " 4. 월이 바뀌면 기존 행 아래에 계속 이어 붙이기(같은 년월 재업로드 시 덮어씀).",
        " 5. [금액]은 ERP 실제 사용금액. 없으면 단가×사용량으로 채워도 됨.",
        "",
        "■ 업로드: 앱 > 데이터 관리 > '② 단가·사용량' 탭에 이 파일 그대로 올리기.",
    ]})
    return _bytes({"단가사용_입력": example, "원료마스터_참고": mm, "작성법": guide})


